export function canTransitionReferralStatus(currentStatus, nextStatus) {
  const current = normalizeReferralStatus(currentStatus);
  const next = normalizeReferralStatus(nextStatus);

  if (current === next) {
    return true;
  }

  const allowed = REFERRAL_STATUS_TRANSITIONS[current];
  return Boolean(allowed && allowed.has(next));
}

export function isFinalReferralStatus(status) {
  const normalized = normalizeReferralStatus(status);

  return (
    normalized === REFERRAL_STATUSES.REJECTED ||
    normalized === REFERRAL_STATUSES.DEAL_LOST ||
    normalized === REFERRAL_STATUSES.AGREED_PERCENT_TRANSFERRED_TO_UJB
  );
}
