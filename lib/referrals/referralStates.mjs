const STATUS_ALIASES = new Map([
  ["received full & final payment", REFERRAL_STATUSES.RECEIVED_FULL_AND_FINAL_PAYMENT],
  [
    "received full and final payment",
    REFERRAL_STATUSES.RECEIVED_FULL_AND_FINAL_PAYMENT,
  ],
  [
    "received part payment & transferred to ujustbe",
    REFERRAL_STATUSES.RECEIVED_PART_PAYMENT_AND_TRANSFERRED_TO_UJB,
  ],
  [
    "received part payment and transferred to ujustbe",
    REFERRAL_STATUSES.RECEIVED_PART_PAYMENT_AND_TRANSFERRED_TO_UJB,
  ],
  ["reject", REFERRAL_STATUSES.REJECTED],
]);

export function normalizeReferralStatus(value) {
  const raw = String(value || "").trim();

  if (!raw) {
    return REFERRAL_STATUSES.PENDING;
  }

  const directMatch = REFERRAL_STATUS_OPTIONS.find((status) => status === raw);

  if (directMatch) {
    return directMatch;
  }

  const alias = STATUS_ALIASES.get(raw.toLowerCase());
  return alias || raw;
}

